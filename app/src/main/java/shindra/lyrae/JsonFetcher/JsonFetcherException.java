public class JsonFetcherException extends Exception {

    public JsonFetcherException() {
    }

    public JsonFetcherException(String message) {
        super(message);
    }

    public JsonFetcherException(String message, Throwable cause) {
        super(message, cause);
    }

    public JsonFetcherException(Throwable cause) {
        super(cause);
    }


}
